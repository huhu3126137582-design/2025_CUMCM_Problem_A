function S = decodeVars_with_gap_(x)
%辅助函数
    S.psi = 2*pi*x(1);          % 用弧度角表示无人机水平航向角
    S.vF  = 70 + 70*x(2);       % FY1无人机速度为 70~140 m/s
    d1 =  0 + 30*x(3);
    d2 = d1 + 1 + (55-(d1+1))*x(4);  % ≥ d1+1
    d3 = d2 + 1 + (66-(d2+1))*x(5);  % ≥ d2+1（≤ ~67）
    S.d = [d1, d2, d3];     % 表示FY1上的三枚弹投放时刻
    S.D = 20 * x(6:8);          % 表示FY1上的三枚弹延时起爆（0~20 s）
    S.e = S.d + S.D;           % 三枚弹起爆时刻
end
